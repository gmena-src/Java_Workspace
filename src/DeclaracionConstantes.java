
public class DeclaracionConstantes {

	public static void main(String[] args) {
		
		final double CONVERSION_EURO = 45.5; //Al declarar una constante 
		
		//Por convencion (palabra reservada "final" + tipo de dato + nombre en mayuscula + inicializacion)
		//Las constantes no pueden ser modificadas durante el flujo de ejecucion
		
		System.out.println(CONVERSION_EURO);
		
	}
	
}
